//Operações Aritiméticas
let v1 = 12.5;
let v2 = 4.5;

console.log("------------------------------------");
console.log("-> Valores: \n-> v1 =", v1, "\n-> v2 =", v2);
console.log("------------------------------------");

//calculando com operadores em linha "Inline Code"
console.log("Soma: \n-> v1 + v2 =", v1 + v2);
console.log("------------------------------------")
console.log("Subtração: \n-> v1 - v2 =", v1 - v2);
console.log("------------------------------------")
console.log("Multiplicação: \n-> v1 x v2 =", v1 * v2);
console.log("------------------------------------")
console.log("Divisão: \n-> v1 / v2 =", v1 / v2);
console.log("------------------------------------")

